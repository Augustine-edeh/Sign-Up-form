// Show/Hide password
function show_Password() {
  var e = document.getElementById("pw");
  var a = document.getElementById("sp");


  if (e.type == "password") {
    e.type = "text";
    a.innerHTML = "Hide password";
  }  else {
    e.type = "password";
    a.innerHTML = "Show password";
  }  
}


